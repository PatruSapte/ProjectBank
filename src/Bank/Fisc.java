    private void jListTrackedValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jListTrackedValueChanged
        // TODO add your handling code here:
        if(jListTracked.getSelectedValue()==null)return;
        c=null;
        jList.clearSelection();
        for(Client it: Client.clientList)
        {
            if(it.username.equals(jListTracked.getSelectedValue().toString())){c=it;}
        }
        jUser.setText(c.username);
        jCnp.setText(c.cnp);
        jRon.setText(String.valueOf(c.sumRon));
        jEuro.setText(String.valueOf(c.sumEuro));
        if(c.isTracked)
        {
            jTracked.setText("Da");
            jTracking.setSelected(true);
        }
        else
        {
            jTracked.setText("Nu");
            jTracking.setSelected(false);
        }
        jData.setModel(c.trackingData);

    }//GEN-LAST:event_jListTrackedValueChanged

