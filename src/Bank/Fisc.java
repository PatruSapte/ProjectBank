    private void jTrackingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTrackingActionPerformed
        // TODO add your handling code here:
        if(jTracking.isSelected())
        {
            trackedClientListNames.addElement(c.username);
            jTracked.setText("Da");
            c.isTracked=true;
            Client.updateClient(c);
        }
        else
        {
            trackedClientListNames.removeElement(c.username);
            jTracked.setText("Nu");
            c.isTracked=false;
            c.trackingData.removeAllElements();
            Client.updateClient(c);
        }
        jListTracked.setModel(trackedClientListNames);
    }//GEN-LAST:event_jTrackingActionPerformed

